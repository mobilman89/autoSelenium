public class Main {
  public static void main(String[] args) {
    System.out.println("Hello world!");

    int value = 19;
    int extra = 25;
    //value <= extra     extra < value      value > -25     value  >=  extra
    System.out.print(value <= extra);
    System.out.print(extra < value);
    System.out.print(value > -25);
    System.out.print(value  >=  extra);


  }
}
